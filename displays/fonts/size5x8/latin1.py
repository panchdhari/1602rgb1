def convert8x5to5x8(char):
	newval = [ ]
	c = 1

	for i in range(0,8):
		v = 0
		nc = 16
		for b in fontpkg[char]:
			if b & c: v = v | nc
			nc = nc / 2
		c = c*2
		newval.append(v)
	return newval
